PI = 3.14159
raio = gets().to_f

volume = (4/3.0) * PI * (raio ** 3)
puts ("VOLUME = %.3f" % volume)


#Nesse código, a constante PI é definida com o valor de Pi (3.14159). 
#Em seguida, é lido um valor do usuário e armazenado na variável raio 
#como um número de ponto flutuante.
#Em seguida, é calculado o volume de uma esfera usando a fórmula 
#volume = (4/3.0) * PI * (raio ** 3). Observe que 4/3.0 é usado em vez 
#de 4/3 para garantir que a divisão seja feita em números de ponto 
#flutuante.
#Por fim, a saída é formatada como uma string que contém o valor do 
#volume com três casas decimais após o ponto decimal, e é impressa na 
#tela com puts ("VOLUME = %.3f" % volume). A expressão "%.3f" % volume é 
#usada para formatar o valor do volume com três casas decimais após o 
#ponto decimal, e a string "VOLUME =" é concatenada antes da saída 
#formatada para imprimir a mensagem completa.
    
    
    
    