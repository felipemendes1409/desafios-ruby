
primeiroValor = gets.to_i

segundoValor = gets.to_i

PROD = primeiroValor * segundoValor

puts "PROD = #{PROD} "

#Esse código lê dois valores do usuário, converte os valores para
#inteiros com o método to_i, armazena em duas variáveis primeiroValor
#e segundoValor, multiplica esses valores e armazena o resultado na 
#constante PROD. Depois, a string "PROD = #{PROD} " é exibida no console,
#com o valor da constante PROD interpolado na string.