
numero = gets.to_i
resultado = numero + 2
puts "O resultado foi: #{resultado}"


#Esse código lê uma entrada do usuário como um número inteiro, 
#adiciona 2 ao número e armazena o resultado na variável resultado. 
#Em seguida, a frase "O resultado foi:" e o valor de resultado são 
#impressos na tela usando interpolação de string. A interpolação de 
#string é feita utilizando #{}.